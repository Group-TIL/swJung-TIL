package section03.exam01_for;

public class ForMultiplicationTableExample {

	public static void main(String[] args) {
		for(int m = 2; m<=9;m++) {
			System.out.println("**** "+m+"단 ****");
			for(int n = 1;n<=9;n++) {
				System.out.println(m+" x "+n+" = "+(m*n));
			}
		}

	}
	/*
	 * while문 : 조건에 따라 반복을 계속할지를 결정할 때 사용
	 * 
	 */

}
